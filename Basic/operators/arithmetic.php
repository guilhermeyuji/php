<?php

$a = 10;
$b = 2;


// SOMA
echo $a + $b; // 12
echo '</br>';

// SUBTRAÇÃO
echo $a - $b; // 8
echo '</br>';

// MULTIPLICAÇÃO
echo $a * $b; // 20
echo '</br>';

// DIVISÃO
echo $a / $b; // 5
echo '</br>';

// MÓDULO (Resto da divisão)
echo $a % $b; // 0
echo '</br>';

// EXPONENCIAL ( 10 ** 3 = 10 * 10 * 10 )
echo $a ** $b; // 100
echo '</br>';

